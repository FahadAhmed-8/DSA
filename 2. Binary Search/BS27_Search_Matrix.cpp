#include <iostream>
#include <vector>

using namespace std;

class Solution {
public:
    bool searchMatrix(vector<vector<int>>& matrix, int target) {
        if (matrix.empty() || matrix[0].empty()) return false;

        int low = 0;
        int m = matrix.size();
        int n = matrix[0].size();
        int high = m * n - 1;
        
        while (low <= high){
            int mid = low + (high - low) / 2;
            int row = mid / n;
            int col = mid % n;
            
            if (matrix[row][col] == target){
                return true;
            } else if(matrix[row][col] < target){
                low = mid + 1;
            } else {
                high = mid - 1;
            }
        }
        return false;
    }
};

int main() {
    Solution s;
    
    vector<vector<int>> matrix = {
        {1, 3, 5, 7},
        {10, 11, 16, 20},
        {23, 30, 34, 60}
    };
    int target = 3;

    if (s.searchMatrix(matrix, target)) {
        cout << "true" << endl;
    } else {
        cout << "false" << endl;
    }

    return 0;
}